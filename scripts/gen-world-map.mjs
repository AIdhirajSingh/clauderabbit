// gen-world-map.mjs — bake REAL world geography into a committed data file.
//
// Reads Natural Earth country boundaries (the canonical `world-atlas`
// countries-110m TopoJSON), projects them equirectangular into the SAME
// MAP_W×MAP_H space as lib/world-geo's `project()` (so a projected dot lands on
// the matching real coastline), and emits lib/world-geo-data.ts. This runs at
// BUILD time only (d3-geo / topojson-client are devDependencies); the runtime
// renders the static path strings, so no geo library ships to the client.
//
// Run: node scripts/gen-world-map.mjs   (regenerate after changing MAP_W/MAP_H).
import { readFileSync, writeFileSync } from "node:fs";
import { feature } from "topojson-client";
import { geoEquirectangular, geoPath, geoGraticule10 } from "d3-geo";

// Must match MAP_W / MAP_H in lib/world-geo.ts (the dot projection space).
const W = 360;
const H = 180;

const topo = JSON.parse(
  readFileSync("node_modules/world-atlas/countries-110m.json", "utf8"),
);
const fc = feature(topo, topo.objects.countries);

// Full-globe equirectangular: x = (lng+180)/360*W, y = (90-lat)/180*H — exactly
// lib/world-geo's project(), so the country borders and the dots share one frame.
const projection = geoEquirectangular()
  .scale(W / (2 * Math.PI))
  .translate([W / 2, H / 2]);
const path = geoPath(projection);

// Round path coordinates to 0.1 unit (≈0.1° here) — real shape, ~half the bytes.
const round = (d) => d.replace(/-?\d+\.\d+/g, (m) => String(Number(Number(m).toFixed(1))));

const countries = fc.features
  .map((f) => ({ name: (f.properties && f.properties.name) || "", d: round(path(f) || "") }))
  .filter((c) => c.d);

const graticule = round(path(geoGraticule10()) || "");

const out = `// AUTO-GENERATED by scripts/gen-world-map.mjs — DO NOT EDIT BY HAND.
// Real Natural Earth country boundaries (world-atlas countries-110m), projected
// equirectangular into the ${W}×${H} map space so they align with lib/world-geo's
// project(). Regenerate with: node scripts/gen-world-map.mjs
export interface CountryPath {
  /** English country name (for accessibility / future per-country interaction). */
  name: string;
  /** SVG path data in the ${W}×${H} equirectangular map space. */
  d: string;
}

/** Every country as a filled SVG path — the real political boundaries. */
export const WORLD_COUNTRIES: readonly CountryPath[] = ${JSON.stringify(countries)};

/** A 10° graticule (lat/lng grid) for a subtle, production-grade backdrop. */
export const WORLD_GRATICULE = ${JSON.stringify(graticule)};
`;

writeFileSync("lib/world-geo-data.ts", out);
console.log(
  `wrote lib/world-geo-data.ts — ${countries.length} countries, ` +
    `${out.length.toLocaleString()} bytes`,
);
